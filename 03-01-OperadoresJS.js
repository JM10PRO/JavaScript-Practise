let a = 3;
let b = 2;
let z = a+b;
console.log("Resultado de la suma " + z);

z = a-b;
console.log("Resultado de la resta " + z);

z = a*b;
console.log("Resultado de la multiplicación " + z);

z = a/b;
console.log("Resultado de la división " + z);

z = a%b;
console.log("Resto de la división (módulo) " + z);

z = a**b;
console.log("Resultado de operador exponente 3 elevado a 2 = " + z);

//Incremento

// pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

// post-incremento (el operador ++ después de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento

// predecremento
z = --a;
console.log(a);
console.log(z);

// postdecremento
z = b--;
console.log(b);
console.log(z);